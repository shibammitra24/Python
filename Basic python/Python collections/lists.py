lst = [21, 68, 65, 87]
print(lst) # print a list
print(type(lst)) # check type of list
print(len(lst)) # print length of list

# check if an item is present in the list:
print(87 in lst)
# using if statement:
if 87 in lst:
    print("Item is present in list")

# check if an item is not present in the list:
print(55 not in lst)
# using if statement:
if 55 not in lst:
    print("Item not present in list")